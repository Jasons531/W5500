/*******************************************************************************
* 函数名  : SPI1_WriteByte
* 描述    : 通过SPI1向指定端口寄存器写4个字节数据
* 输入    : s:端口号,reg:16位寄存器地址,*dat_ptr:待写入的4个字节缓冲区指针
* 输出    : 无
* 返回值  : 无
* 说明    : 无
*******************************************************************************/
void SPI1_WriteByte(unsigned char dat)
{
    HAL_StatusTypeDef status = HAL_OK;

    status = HAL_SPI_Transmit(&hspi1, (uint8_t*)&dat, 1, 0xFFFF);

	/* Check the communication status */
	if(status != HAL_OK)
	{
	/* Re-Initiaize the BUS */
		 printf("SPI1_Write_error\r\n");
		 Error_Handler();
	}
}

/*******************************************************************************
* 函数名  : SPI1_ReadByte
* 描述    : 读W5500指定地址寄存器的1个字节数据
* 输入    : reg:16位寄存器地址
* 输出    : 无
* 返回值  : 读取到寄存器的1个字节数据
* 说明    : 无
*******************************************************************************/
uint8_t SPI1_ReadByte(void)
{
    HAL_StatusTypeDef status = HAL_OK;
	uint32_t readvalue = 0;

	status = HAL_SPI_Receive(&hspi1, (uint8_t*) &readvalue, 1, 0xFFFF);

	/* Check the communication status */
	if(status != HAL_OK)
	{
		/* Re-Initiaize the BUS */
		printf("SPI1_Read_error = %d\r\n",status);
		Error_Handler();
	}

	return readvalue;
}

